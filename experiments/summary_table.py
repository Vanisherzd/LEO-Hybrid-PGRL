def make_booktabs(data, col_fmt="lcccc", label="tab:main"):
    """Build a booktabs-style LaTeX table string.

    data keys: header, rows, note, caption
    label: LaTeX \\label{} value (default 'tab:main')
    """
    headers = data["header"]
    rows    = data["rows"]

    lines = [
        "\\begin{table}[htbp]",
        "\\centering",
        f"\\caption{{\\label{{{label}}}{data.get('caption', 'Results Summary')}}}",
        f"\\begin{{tabular}}{{{col_fmt}}}",
        "\\toprule",
        " & ".join(headers) + " \\\\",
        "\\midrule",
    ]
    for row in rows:
        lines.append(" & ".join(str(c) for c in row) + " \\\\")

    lines.extend([
        "\\bottomrule",
        "\\end{tabular}",
        f"\\justify\\small\\textit{{{data.get('note', '')}}}",
        "\\end{table}",
    ])
    return "\n".join(lines)


